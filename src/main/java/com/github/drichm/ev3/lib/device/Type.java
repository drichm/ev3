package com.github.drichm.ev3.lib.device;

/** Attribute value types */
public enum Type
{
  /** Port address */
  a,

  /** String text */
  s,
  
  /** Space separated list of strings */
  sl,

  /** Integer as a string */
  si,

  /** Float/Double as a string */
  sf,
  
  /** Array of bytes */
  b
}
